public class MyClasswork {
    public static void main(String[] args) {
        // Example Java code to demonstrate basic functionality
        int sum = 0;
        for (int i = 1; i <= 5; i++) {
            if (i % 2 == 0) {
                sum += i;
            }
        }
        System.out.println("The sum of even numbers between 1 and 5 is: " + sum);
    }
}
